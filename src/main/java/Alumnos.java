    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String numeroS = Numeroalum.getText();
        String nombre_alu = Nombre.getText();
        String notaS = Nota.getText();
        int nota_alu = Integer.parseInt(notaS);
        int numero_alu = Integer.parseInt(numeroS);
        boolean existe = false;
       
        
        for (Alumno alumno : Alumnos) {

            if (alumno.getNumero() == numero_alu) {

                JOptionPane.showMessageDialog(null, " Ya existe un alumno con ese codigo");
                existe = true;
                break;
            }
        }
        if(existe == false){
            Alumno alumno = new Alumno(nota_alu, nombre_alu, numero_alu);
            Alumnos.add(alumno);
            JOptionPane.showMessageDialog(null,alumno.getNumero() + " el alumno " + alumno.getNombre() + " con nota " + alumno.getNota() + " ha sido a√±adido con exito");

        }else{
            
        }
        
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed

        String nombre = NombreBuscar.getText();

        for (Alumno alumno : Alumnos) {

            if (alumno.getNombre().equals(nombre)) {

                Encontrado.setText(alumno.getNombre() + " con nota " + alumno.getNota());
            }
        }


    }//GEN-LAST:event_jButton5ActionPerformed

